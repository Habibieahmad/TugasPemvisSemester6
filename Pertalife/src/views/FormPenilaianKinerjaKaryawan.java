    private void dataNilaiKinerja(){
        try{
            Statement stat = conn.createStatement();
            String sqlN1 = "SELECT * FROM Asuransi_Individu WHERE kd_nilai = 'N1'";            
            String sqlN2 = "SELECT * FROM Asuransi_Individu WHERE kd_nilai = 'N2'";            
            String sqlN3 = "SELECT * FROM Asuransi_Individu WHERE kd_nilai = 'N3'";            
            ResultSet rs1 = stat.executeQuery(sqlN1);
            if (rs1.next()) {
                jTN1.setText("Nilai "+rs1.getString("nama_penilaian")+" (N1)");
                jN1.setText(rs1.getInt("nilai_bobot")+"%");
                ResultSet rs2 = stat.executeQuery(sqlN2);
                if (rs2.next()) {
                    jTN2.setText("Nilai "+rs2.getString("nama_penilaian")+" (N2)");
                    jN2.setText(rs2.getInt("nilai_bobot")+"%");
                    ResultSet rs3 = stat.executeQuery(sqlN3);
                    if (rs3.next()) {
                        jTN3.setText("Nilai "+rs3.getString("nama_penilaian")+" (N3)");
                        jN3.setText(rs3.getInt("nilai_bobot")+"%");
                    }
                }
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"<html><p style=\"font-size:11px; width:200px;\"><b style=\"font-size:14px;\">Kode Penjualan Failed!</b><br>"+e+"</p></html>","Failed!",JOptionPane.ERROR_MESSAGE,iconError);
        }
    }
    
