    private void bHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungActionPerformed
        // TODO add your handling code here:        
        String strjN1 = jN1.getText().replace("%", "");        
        String strjN2 = jN2.getText().replace("%", "");        
        String strjN3 = jN3.getText().replace("%", "");        
        double n1,n2,n3,jn1,jn2,jn3,xn1,xn2,xn3,xnTotal;
        n1 = Integer.parseInt(tn1.getText());        
        jn1 = Integer.parseInt(strjN1);        
        xn1 = n1 * (jn1/100);
        String xtn1 = Double.toString(xn1);
        txn1.setText(xtn1);
        n2 = Integer.parseInt(tn2.getText());        
        jn2 = Integer.parseInt(strjN2);        
        xn2 = n2 * (jn2/100);
        String xtn2 = Double.toString(xn2);
        txn2.setText(xtn2);
        n3 = Integer.parseInt(tn3.getText());        
        jn3 = Integer.parseInt(strjN3);        
        xn3 = n3 * (jn3/100);
        String xtn3 = Double.toString(xn3);
        txn3.setText(xtn3);
        xnTotal = xn1 + xn2 + xn3;
        String sxnTotal = Double.toString(xnTotal);
        txnTotal.setText(sxnTotal);
    }//GEN-LAST:event_bHitungActionPerformed

