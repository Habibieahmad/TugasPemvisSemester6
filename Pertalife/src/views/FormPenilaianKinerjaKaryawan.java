    private void kodePenilaian(){
        txtKdPenilaian.setEnabled(false);
        try {
            String sql="SELECT * FROM penilaian_kinerja_karyawan ORDER BY kode_penilaian DESC";
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            if (rs.next()) {
                String kp = rs.getString("kode_penilaian").substring(3);
                String AN = "" + (Integer.parseInt(kp) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "0000";}
                else if(AN.length()==2)
                {Nol = "000";}
                else if(AN.length()==3)
                {Nol = "00";}
                else if(AN.length()==4)
                {Nol = "0";}
                else if(AN.length()==5)
                {Nol = "";}
               txtKdPenilaian.setText("KDP" + Nol + AN);
            } else {
               txtKdPenilaian.setText("KDP00001");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"<html><p style=\"font-size:11px; width:200px;\"><b style=\"font-size:14px;\">Kode Penjualan Failed!</b><br>"+e+"</p></html>","Failed!",JOptionPane.ERROR_MESSAGE,iconError);
        }
    }

